// faster version of if else(switch)
const color = 'yellow';
switch (color) {
    case 'black':
        console.log('its black');
        break;
    case 'white':
        console.log('its white');
        break;
    case 'blue':
        console.log('its blue');
        break;
    case 'green':
        console.log('its green');
        break;
    case 'yellow':
        console.log('finally its yellow');
        break;
    default:
        console.log('its nothing');
}